package Data;

public class ChapData {
    private String chapName;
    private int chapId;
    private int wpId;

    public String getChapName() {
        return chapName;
    }

    public void setChapName(String chapName) {
        this.chapName = chapName;
    }

    public int getChapId() {
        return chapId;
    }

    public void setChapId(int chapId) {
        this.chapId = chapId;
    }

    public int getWpId() {
        return wpId;
    }

    public void setWpId(int wpId) {
        this.wpId = wpId;
    }
}
